#include "push_swap.h"

void	ft_sort_three(t_stack *s1)
{
	t_stack	*s2;
	t_stack	*s3;

	s2 = s1->next;
	s3 = s2->next;
	if (s1 < s2 && s2 < s3)
		return ;
}
